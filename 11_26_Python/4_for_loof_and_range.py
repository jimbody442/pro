#반복 횟수만 지정, 시작값은 기본값 0
print('My name is')
for i in range(5):
    print('jimin five times('+str(i)+')')

#시작값, 종료값, 증가값 설정
print('time i * 2, end number 100')
for i in range(0,100,2):
    print('jimin five times('+str(i)+')')



#range값이 차가될 수 있다.
print('time i -1, end number 10~0')
for i in range(10,-1,-1):
    print('jimin five times('+str(i)+')')